package fundamentos;

public class Exercicio01_Produto {
	
	int quantidade;
	double valor;
	
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	public int getQuantidade() {
		return quantidade;
	}
	
	public double getValor() {
		return valor;
	}
	

}
